type MultiLangCodeProps = {
  codes: Partial<Record<Language, string>>;
  defaultLang?: Language;
};

export function MultiLangCode({ codes, defaultLang = "python" }: MultiLangCodeProps) {
  const availableLanguages = languages.filter((lang) => codes[lang.id]);
  const [activeLang, setActiveLang] = useState<Language>(
    codes[defaultLang] ? defaultLang : (availableLanguages[0]?.id ?? "python")
  );

  const activeCode = codes[activeLang] ?? "";

  return (
    <div className="space-y-3">
      <div className="flex items-center gap-2">
        <p className="text-xs font-medium uppercase tracking-[0.1em] text-text-muted">Code Example</p>
        <span className="h-px flex-1 bg-border" />
      </div>

      {/* Language Tabs - Premium pill selector */}
      <div className="bezel-outer p-1">
        <div className="flex flex-wrap gap-1">
          {availableLanguages.map((lang) => (
            <button
              key={lang.id}
              type="button"
              onClick={() => setActiveLang(lang.id)}
              className={`group flex items-center gap-2 rounded-full px-3 py-2 text-xs font-medium transition-all duration-500 ease-[cubic-bezier(0.32,0.72,0,1)] ${
                activeLang === lang.id
                  ? "bg-accent/10 text-accent shadow-[inset_0_1px_1px_rgba(16,185,129,0.1)]"
                  : "text-text-muted hover:bg-white/5 hover:text-foreground"
              }`}
            >
              <span
                className={`transition-colors duration-300 ${
                  activeLang === lang.id ? "text-accent" : "text-text-muted group-hover:text-foreground"
                }`}
              >
                {lang.icon}
              </span>
              <span>{lang.label}</span>
            </button>
          ))}
        </div>
      </div>

      {/* Code Block */}
      <div className="code-block relative overflow-hidden">
        {/* Active language badge */}
        <div className="absolute right-3 top-3 z-10">
          <div className="flex items-center gap-1.5 rounded-full bg-white/5 px-2.5 py-1 text-[10px] font-medium uppercase tracking-[0.1em] text-text-muted backdrop-blur-sm">
            {availableLanguages.find((l) => l.id === activeLang)?.icon}
            <span>{availableLanguages.find((l) => l.id === activeLang)?.label}</span>
          </div>
        </div>

        <pre className="overflow-x-auto p-5 pt-12 text-[13px] leading-relaxed text-text-secondary">
          <code>{activeCode}</code>
        </pre>

        {/* Copy button */}
        <button
          type="button"
          onClick={() => navigator.clipboard.writeText(activeCode)}
          className="absolute bottom-3 right-3 flex h-8 w-8 items-center justify-center rounded-lg bg-white/5 text-text-muted transition-all duration-300 hover:bg-white/10 hover:text-foreground active:scale-95"
          title="Copy code"
        >
          <svg className="h-4 w-4" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              d="M15.666 3.888A2.25 2.25 0 0013.5 2.25h-3c-1.03 0-1.9.693-2.166 1.638m7.332 0c.055.194.084.4.084.612v0a.75.75 0 01-.75.75H9a.75.75 0 01-.75-.75v0c0-.212.03-.418.084-.612m7.332 0c.646.049 1.288.11 1.927.184 1.1.128 1.907 1.077 1.907 2.185V19.5a2.25 2.25 0 01-2.25 2.25H6.75A2.25 2.25 0 014.5 19.5V6.257c0-1.108.806-2.057 1.907-2.185a48.208 48.208 0 011.927-.184"
            />
          </svg>
        </button>
      </div>
    </div>
  );
}
